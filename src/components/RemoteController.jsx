window.addEventListener('keydown', (event) => {
    switch(event.code) {
      case 'ArrowDown':
        // вниз
        break;
       case 'ArrowUp':
        // вверх
        break;
       case 'ArrowLeft':
        // влево
        break;
       case 'ArrowRight':
        // вправо
        break;
       case 'Enter':
        // ок
       break;
    }
  });